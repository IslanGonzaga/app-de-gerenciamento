    private void consultarId(){
        String sql = "select * from funcionarios where idfunc = ?";
        int setar = tblVsgFun.getSelectedRow();
        txtVsgFunId.setText(tblVsgFun.getModel().getValueAt(setar, 0).toString());
        txtVsgFunNom.setText(tblVsgFun.getModel().getValueAt(setar, 1).toString());
        cboVsgFunCar.setSelectedItem(tblVsgFun.getModel().getValueAt(setar, 2).toString());
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,tblVsgFun.getModel().getValueAt(setar, 0).toString());
            rs=pst.executeQuery();
            if (rs.next()) {
                txtVsgFunFon.setText(rs.getString(4));
                txtVsgFunDen.setText(rs.getString(6));
                txtVsgFunDsa.setText(rs.getString(7));
                txtVsgFunCep.setText(rs.getString(8));
                txtVsgFunEnd.setText(rs.getString(9));
            } else {
                JOptionPane.showMessageDialog(null, "Funcion√°rio n√£o cadastrado.");
                txtVsgFunId.setText(null);
                txtVsgFunNom.setText(null);
                cboVsgFunCar.setSelectedItem(1);
                txtVsgFunFon.setText(null);
                txtVsgFunDen.setText(null);
                txtVsgFunDsa.setText(null);
                txtVsgFunCep.setText(null);
                txtVsgFunEnd.setText(null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void consultaNome(){
        String sql = "select idfunc,nomefunc,cargo from funcionarios where nomefunc like ?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,txtVsgFunPesNom.getText()+"%");
            rs=pst.executeQuery();
            // biblioteca rs2xml.jar;
            tblVsgFun.setModel(DbUtils.resultSetToTableModel(rs));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
